    public void aksilihat(){
        kmk.tblkmk = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        kmk.tblkmk.setColumnIdentifiers(new Object[]
        {"<html><font face='Baloo' size=4>Kode Kelompok Mata Kuliah</font></html>", 
            "<html><font face='Baloo' size=4>Kelompok Mata Kuliah</font></html>"});
        
        tbDataKMK.setModel(kmk.tblkmk);
        JTableHeader tabelheader = tbDataKMK.getTableHeader();
            tabelheader.setForeground(Color.WHITE);
            tabelheader.setBackground(new Color(0, 120, 63));
        int row = kmk.tblkmk.getRowCount();
        for(int i=0 ; i<row ; i++){
            kmk.tblkmk.removeRow(0);
        }
        kmk.select();
        kmk.list.forEach((obj) -> {
            kmk.tblkmk.addRow((Object[]) obj);
        });
        tbDataKMK.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        com.inpos.tools.AutoFitTableColumns tca = new com.inpos.tools.AutoFitTableColumns(tbDataKMK);
        tca.adjustColumns();
        lblRecord.setText("Record : " + tbDataKMK.getRowCount());
    }
    
    public void aksisimpan(){
         if(txtKdKMK.getText().equals("") || txtKMK.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Mohon lengkapi data",
                    "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }else{
            kmk.isUpdate = !lblSimpan.getText().equals("Simpan");
            kmk.kd_kmk = txtKdKMK.getText();
            kmk.kelompok_mk = txtKMK.getText();
            
            kmk.insert_update();
            if(lblSimpan.getText().equals("Ubah")){
                JOptionPane.showMessageDialog(this, "Data berhasil diubah",
                        "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Data berhasil disimpan",
                        "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }
            clearInput(); disableInput(); aksilihat();
        }
    }
    
    public void aksihapus(){
        if(txtKdKMK.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Informasi",
                    "Anda belum memilih data yang akan dihapus", JOptionPane.INFORMATION_MESSAGE);
        }else{
            int jawab = JOptionPane.showConfirmDialog(this, "Apakah Anda akan menghapus data ini? Kode "+kmk.kd_kmk,
                    "Konfirmasi",JOptionPane.YES_NO_OPTION);
            if(jawab == JOptionPane.YES_OPTION){
                kmk.delete(kmk.kd_kmk);
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus",
                        "Informasi", JOptionPane.INFORMATION_MESSAGE);
                clearInput(); 
                disableInput(); 
                aksilihat();
            }
        }
    }
    
