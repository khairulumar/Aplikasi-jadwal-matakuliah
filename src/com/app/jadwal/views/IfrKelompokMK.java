    private void tbDataKMKMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDataKMKMouseClicked
        if(evt.getClickCount() == 2){
            int row = tbDataKMK.getSelectedRow();
            kmk.kd_kmk = tbDataKMK.getValueAt(row, 0).toString();
            kmk.kelompok_mk = tbDataKMK.getValueAt(row, 1).toString();
            
            txtKdKMK.setText(kmk.kd_kmk);
            txtKMK.setText(kmk.kelompok_mk);
            
            txtKdKMK.setEditable(false);
            lblHapus.setEnabled(true);
            lblSimpan.setEnabled(true);
            lblSimpan.setText("Ubah");
            lblSimpan.setIcon(new javax.swing.ImageIcon(getClass().getResource("/com/app/jadwal/icons/update.png")));
            enableInput();
            
        }
    }//GEN-LAST:event_tbDataKMKMouseClicked

    private void txtKdKMKKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtKdKMKKeyTyped
        char enter=evt.getKeyChar();
        if(txtKdKMK.getText().length()==4){
            evt.consume();
        }else if((Character.isDigit(enter))){
            evt.consume();
        }
    }//GEN-LAST:event_txtKdKMKKeyTyped

