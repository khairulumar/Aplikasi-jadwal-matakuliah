    public void aksilihat(){
        gd.tblgedung = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        gd.tblgedung.setColumnIdentifiers(new Object[]
        {"<html><font face='Baloo' size=4>Kode Gedung</font></html>", 
            "<html><font face='Baloo' size=4>Nama Gedung</font></html>"});
        
        tbDataGedung.setModel(gd.tblgedung);
        JTableHeader tabelheader = tbDataGedung.getTableHeader();
            tabelheader.setForeground(Color.WHITE);
            tabelheader.setBackground(new Color(0, 120, 63));
        int row = gd.tblgedung.getRowCount();
        for(int i=0 ; i<row ; i++){
            gd.tblgedung.removeRow(0);
        }
        gd.select();
        gd.list.forEach((obj) -> {
            gd.tblgedung.addRow((Object[]) obj);
        });
        tbDataGedung.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        com.inpos.tools.AutoFitTableColumns tca = new com.inpos.tools.AutoFitTableColumns(tbDataGedung);
        tca.adjustColumns();
        lblRecord.setText("Record : " + tbDataGedung.getRowCount());
    }
    
    public void aksisimpan(){
        if(txtKdGedung.getText().equals("") || txtNamaGedung.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Mohon lengkapi data",
                    "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }else{
            gd.isUpdate = !lblSimpan.getText().equals("Simpan");
            gd.kd_gedung = txtKdGedung.getText();
            gd.nama_gedung = txtNamaGedung.getText();
            
            gd.insert_update();
            if(lblSimpan.getText().equals("Ubah")){
                JOptionPane.showMessageDialog(this, "Data berhasil diubah",
                        "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Data berhasil disimpan",
                        "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }
            clearInput(); disableInput(); aksilihat();
        }
    }
    
    public void aksihapus(){
        if(txtKdGedung.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Informasi",
                    "Anda belum memilih data yang akan dihapus", JOptionPane.INFORMATION_MESSAGE);
        }else{
            int jawab = JOptionPane.showConfirmDialog(this, "Apakah Anda akan menghapus data ini? Kode "+gd.kd_gedung,
                    "Konfirmasi",JOptionPane.YES_NO_OPTION);
            if(jawab == JOptionPane.YES_OPTION){
                gd.delete(gd.kd_gedung);
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus",
                        "Informasi", JOptionPane.INFORMATION_MESSAGE);
                clearInput(); 
                disableInput(); 
                aksilihat();
            }
        }
    }

