    private void tbDataGedungMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDataGedungMouseClicked
        if(evt.getClickCount() == 2){
            int row = tbDataGedung.getSelectedRow();
            gd.kd_gedung = tbDataGedung.getValueAt(row, 0).toString();
            gd.nama_gedung = tbDataGedung.getValueAt(row, 1).toString();
            
            txtKdGedung.setText(gd.kd_gedung);
            txtNamaGedung.setText(gd.nama_gedung);
            
            txtKdGedung.setEnabled(false);
            lblHapus.setEnabled(true);
            lblSimpan.setEnabled(true);
            lblSimpan.setText("Ubah");
            lblSimpan.setIcon(new javax.swing.ImageIcon(getClass().getResource("/com/app/jadwal/icons/update.png")));
            enableInput();
            
        }
    }//GEN-LAST:event_tbDataGedungMouseClicked

